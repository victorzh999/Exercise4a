
public class JokeTag {

	private int jokeID;
	private String tag;

	public JokeTag() {
	}

	public JokeTag(int jokeID, String tag) {
		this.jokeID = jokeID;
		this.tag = tag;
	}

	public int getJokeID() {
		return jokeID;
	}

	public void setJokeID(int jokeID) {
		this.jokeID = jokeID;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}
	
}
